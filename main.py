g9 = data.groupby("–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏–µ")["–º–µ—Å—Ç–æ —Ä–∞–±–æ—Ç—ã"].apply(lambda x: (x != "–Ω–µ—Ç –º–µ—Å—Ç–∞ —Ä–∞–±–æ—Ç—ã").mean())
