g1 = data.groupby("–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏–µ")["—Å—Ä–µ–¥–Ω—è—è –∑–∞—Ä–ø–ª–∞—Ç–∞ –≤ –≥–æ–¥"].mean().sort_values(ascending=False)
