# üîü –°—Ä–µ–¥–Ω—è—è –∑–∞—Ä–ø–ª–∞—Ç–∞ –∑–∞–≤–∏—Å–∏—Ç –æ—Ç —Å—É–¥–∏–º–æ—Å—Ç–∏ –∏ –æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏—è
g10 = data.groupby(["–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏–µ", "—Å—É–¥–∏–º–æ—Å—Ç–∏"])["—Å—Ä–µ–¥–Ω—è—è –∑–∞—Ä–ø–ª–∞—Ç–∞ –≤ –≥–æ–¥"].mean()
